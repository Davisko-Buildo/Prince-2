// Filtrage par jour
export const getModulesByDay = (day: 1 | 2): Module[] => {
  return courseModules.filter(module => module.day === day);
};

export const getLessonsByDay = (day: 1 | 2): Lesson[] => {
  return getModulesByDay(day).flatMap(module => module.lessons);
};

export const getTotalLessonsCountByDay = (day: 1 | 2): number => {
  return getLessonsByDay(day).length;
};

export const getAllLessons = (): Lesson[] => {
  return courseModules.flatMap(module => module.lessons);
};

export const getLessonById = (lessonId: string): Lesson | undefined => {
  return getAllLessons().find(lesson => lesson.id === lessonId);
};

export const getModuleByLessonId = (lessonId: string): Module | undefined => {
  return courseModules.find(module => 
    module.lessons.some(lesson => lesson.id === lessonId)
  );
};

export const getNextLesson = (currentLessonId: string): Lesson | null => {
  const allLessons = getAllLessons();
  const currentIndex = allLessons.findIndex(l => l.id === currentLessonId);
  if (currentIndex === -1 || currentIndex === allLessons.length - 1) return null;
  return allLessons[currentIndex + 1];
};

export const getPreviousLesson = (currentLessonId: string): Lesson | null => {
  const allLessons = getAllLessons();
  const currentIndex = allLessons.findIndex(l => l.id === currentLessonId);
  if (currentIndex <= 0) return null;
  return allLessons[currentIndex - 1];
};

export const getTotalLessonsCount = (): number => {
  return getAllLessons().length;
};
